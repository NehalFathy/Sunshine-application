package com.example.android.sunshine.app;

import android.test.suitebuilder.TestSuiteBuilder;

import junit.framework.Test;
import junit.framework.TestSuite;

//contain code to include all of the java test classes into a suite of tests the junit will run
//can easily add aditional tests by just adding additional java class file to test directory
//isn't project specific .. can literally copy and paste it into each project
public class FullTestSuite extends TestSuite {
    public static Test suite() {
        return new TestSuiteBuilder(FullTestSuite.class)
                .includeAllPackagesUnderHere().build();
    }

    public FullTestSuite() {
        super();
    }
}
